class SwitchEx
{

    void s1()
    {
        var num=3;

        switch(num)
        {
          case 1:print("English");
          break;

          case 2:print("Hindi");
          break;

          case 3:print("Guj");
          break;

          default:print("Number is not valid");
          break;

        }

    }

}